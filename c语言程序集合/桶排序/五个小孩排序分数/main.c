//五个同学分别得了5分、3分、5分、2分、8分，(满分10分)从大到小排序
/********
桶排序是把所以分数类型作为桶
所得分数为球、往里面投
每投一次、桶所对应的数从0往上涨
最后打印出桶所对应的个数的分数
排序之精髓在于从小桶到大桶(或大桶到小桶)一一打印
*********/
#include<stdio.h>
int main(void)
{
	int k = 0;
	int arr[11] = {0};
	for (int i = 0; i < 5; i++)
	{
		printf_s("请输入第%d个分数：",i+1);
		scanf_s("%d",&k);
		arr[k]++;
	}
	for (int i = 0; i < 10; i++)
	{
		for (int j = 0; j < arr[i]; j++)
		{
			printf_s("%d",i);
		}
	}
	return 0;
}